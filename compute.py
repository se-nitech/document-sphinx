"""基本的な算術演算のユーティリティ関数。"""


def myadd(a, b):
    """2つの値を加算する。

    Args:
        a (int): 1つ目のオペランド。
        b (int): 2つ目のオペランド。

    Returns:
        int: 2つのオペランドの合計。
    """
    return a + b


def mymult(a, b):
    """2つの値を乗算する。

    Args:
        a (int): 1つ目のオペランド。
        b (int): 2つ目のオペランド。

    Returns:
        int: 2つのオペランドの積。
    """

    return a * b
